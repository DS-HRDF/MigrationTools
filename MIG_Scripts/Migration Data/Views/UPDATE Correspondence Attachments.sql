UPDATE IO_OUTGOINGDESTINATIONS

SET OLD_DOC_ID = outDest.OLD_DOC_ID 

FROM IO_OUTGOINGDESTINATIONS outDest
JOIN MIG_IO_OUTGOING_DESTINATIONS_VIEW outDestV 
ON outDestV.CORRESPONDENCENUMBER = outDest.CORRESPONDENCENUMBER
AND outDestV.HIJRICYEAR = outDest.HIJRICYEAR
AND outDestV.CORRESPONDENCEDESTINATIONID = outDest.CORRESPONDENCEDESTINATIONID

WHERE outDest.CORRESPONDENCENUMBER = outDestV.CORRESPONDENCENUMBER
AND outDest.HIJRICYEAR = outDestV.HIJRICYEAR   
AND outDestV.CORRESPONDENCEDESTINATIONID = outDest.CORRESPONDENCEDESTINATIONID
AND outDest.IS_MIGRATED = 1


-----------------------------------------------------------------------------------------------------------


UPDATE IO_EMPLOYEES 

SET MigratedUserId = empV.MigratedUserId 

FROM IO_EMPLOYEES emps 
JOIN MIG_IO_EMPLOYEES_VIEW empV
ON emps.EMPLOYEEID = empV.EMPLOYEEID
WHERE emps.EMPLOYEEID = empV.EMPLOYEEID


-----------------------------------------------------------------------------------------------------------


UPDATE IO_FORWARDINGSHISTORY 

SET FORWARDINGTIME = fhV.FORWARDINGTIME 

FROM IO_FORWARDINGSHISTORY fh
JOIN MIG_IO_FORWARDINGSHISTORY_VIEW fhV 
ON fh.CORRESPONDENCENUMBER = fhV.CORRESPONDENCENUMBER
AND fh.HIJRICYEAR = fhV.HIJRICYEAR
AND fh.FORWARDINGID = fhV.FORWARDINGID
AND fh.TYPEID = fhV.TYPEID

WHERE fh.CORRESPONDENCENUMBER = fhV.CORRESPONDENCENUMBER
AND fh.HIJRICYEAR = fhV.HIJRICYEAR
AND fh.FORWARDINGID = fhV.FORWARDINGID
AND fh.TYPEID = fhV.TYPEID

