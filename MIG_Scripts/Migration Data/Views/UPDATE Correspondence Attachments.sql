
UPDATE IO_INTERNAL 

SET CORRESPONDENCEATTACHMENTS = interV.CORRESPONDENCEATTACHMENTS 

FROM IO_INTERNAL inter
JOIN MIG_IO_INTERNAL_VIEW interV 
ON inter.CORRESPONDENCENUMBER = interV.CORRESPONDENCENUMBER
AND inter.HIJRICYEAR = interV.HIJRICYEAR

WHERE inter.CORRESPONDENCENUMBER = interV.CORRESPONDENCENUMBER
AND inter.HIJRICYEAR = interV.HIJRICYEAR


