package com.dataserve.migration.spga.objects;

public class UserInstructions {
	private String contactId, contactType, contactInstruction;

	public String getContactId() {
		return contactId;
	}

	public void setContactId(String contactId) {
		this.contactId = contactId;
	}

	public String getContactType() {
		return contactType;
	}

	public void setContactType(String contactType) {
		this.contactType = contactType;
	}

	public String getContactInstruction() {
		return contactInstruction;
	}

	public void setContactInstruction(String contactInstruction) {
		this.contactInstruction = contactInstruction;
	}

}
